  <h1>Normal Aging Changes </h1>
  <div class="content">
    <p class="subheader">Hearing Changes</p>
    <p>More than half of people aged 60 or over are hard of hearing or deaf. Presbycusis is the most common form of hearing loss and is thought to be due to the combined effects of aging of the peripheral or central auditory systems, and the accumulated effects of wear-and-tear. </p>
    <p>Older adults may compensate for hearing loss by using hearing aids. However, some older adults don’t recognize their problem and some deny they have problems and thus don’t take actions to correct hearing impairments resulting in potential communication problems. These communication problems can lead to social isolation.</p>
    <p>Strategies for Compensating for Hearing Changes </p>
    <ul>
      <li>Stand or sit in front of the older adult; get the older adult’s attention. </li>
      <li>Speak up but do not shout. </li>
      <li>Use lower-pitched tones. They are heard more easily than higher ones. </li>
      <li>Speak slowly and clearly, and emphasize only key words. </li>
      <li>Cut out as much background noise as possible. </li>
      <li>Keep your mouth in clear view, and maintain eye contact if possible. </li>
      <li>Rephrase rather than repeat a misunderstood sentence. Allow a few seconds pause after every few sentences to allow for processing and to allow the listener to formulate questions.</li>
    </ul>
    <p class="subheader">Vision Changes</p>
    <p>About 7.3 million or 21% of persons age 65 and over report some form of vision impairment. As baby boomers age, this number will reach 8.3 million in the year 2010, 11.3 million in 2020, and 14.8 million in 2030.</p>
    <p>Vision loss may be due to: </p>
    <ul>
      <li>Decreased pupil size and accommodation which alter visual accuracy </li>
      <li>Macular degeneration which impedes central vision </li>
      <li>Glaucoma which impeded peripheral vision </li>
      <li>Cataracts which cloud vision.</li>
    </ul>
    <p>Strategies for Compensating for Vision Changes</p>
    <p>To accommodate older adults with visual impairments, it is important to make give clear verbal cues to assist the older adult.</p>
    <ul>
      <li>If you are entering a room with someone who is visually impaired, describe the room layout, other people who are in the room, and what is happening. </li>
      <li>Tell the person if you are leaving. Let him/her know if others will remain in the room or if he/she will be alone. </li>
      <li>Allow the person to take your arm for guidance. </li>
      <li>When you speak, let the person know whom you are addressing; </li>
      <li>Ask how you may help: increasing the light, reading the menu, describing where things are, or in some other way. </li>
      <li>Call out the person’s name before touching; touching lets a person know that you are listening. </li>
      <li>Treat him/her like a sighted person as much as possible. </li>
      <li>Explain what you are doing as you are doing it. </li>
      <li>Leave things where they are unless the person asks you to move something.</li>
    </ul>
    <p class="subheader">Changes in Smell and Taste</p>
    <p>Decline in smell and taste are also normal aging sensory changes. Older adults recognize and identify common odors less well. Loss of taste and smell are common and result from: </p>
    <ul>
      <li>Normal aging from the degeneration of the taste buds, and decreased saliva production </li>
      <li>Certain disease states, such as Alzheimer’s disease </li>
      <li>Medications </li>
      <li>Surgical interventions </li>
      <li>Environmental exposure</li>
    </ul>
    <p>Sensory losses of taste and smell can cause: </p>
    <ul>
      <li>Reluctance to talk about food </li>
      <li>Reduced pleasure and comfort from food affecting the socialization that accompanies eating </li>
      <li>Increased nutritional and immune deficiencies </li>
      <li>Reduced adherence to dietary regimen.</li>
    </ul>
    <p>Older adults should be encouraged to have routine oral care and dental visits and to use dentures regularly. To accommodate declines in smell and taste, meals should have pleasing colors, textures, and flavorings to make them look and taste more appealing.</p>
    <p>Common Health Issues of Older Adults</p>
    <p>According to Older Americans 2008: Key Indicators of Well-Being, published by the <a href="../empower/www.agingstats.gov">Federal    Interagency Forum on Age Related Statistics</a>, about 80% of older adults have at least one age- related chronic health condition and 50% have at least two. The most prevalent include: </p>
    <ul>
      <li>Arthritis </li>
      <li>Hypertension </li>
      <li>Heart Disease </li>
      <li>Stroke </li>
      <li>Diabetes </li>
      <li>Cancer </li>
      <li>Osteoporosis </li>
      <li>Alzheimer’s Disease</li>
    </ul>
    <p>Arthritis encompasses more than 100 diseases and conditions that affect joints, surrounding tissues, and other connective tissues. It is the leading cause of disability among older adults. Approximately 43% of all men and 54% of all women over the age of 65 have some level of arthritis.</p>
    <p>Hypertension is also prevalent among older adults and can lead to activity limitations. 52% of all men and 54% of all women over the age of 65 suffer from hypertension.</p>
    <p>Heart Disease and Stroke. Although older women are more likely to have hypertension than older men, the prevalence of heart disease and stroke is higher among older men. 37% of men and 26% of older women have heart disease, while 10% of older men and 8% of older women have suffered a stroke.</p>
    <p>Diabetes also affects the health of older adults and limits their ability to perform normal activities. 19% of men and 17% of women over the age of 65 suffer from diabetes.</p>
    <p>Cancer. Older men are at a greater risk of cancer than older women, 24% of older men and 19% of older women having some form of cancer. For men, the most commonly diagnosed cancers included prostate, lung, colon, and rectum. Among women, cancers of the breast, colon, and rectum were the most common.</p>
    <p>Osteoporosis, another common chronic ailment among older adults, reduces bone density and raises the risk for potential disabling fractures. Although women are four times more likely than men to experience bone loss, recent research suggests that the prevalence of osteoporosis among men has been significantly under-diagnosed and under-reported.</p>
    <p>Alzheimer’s Disease is a progressive, degenerative disease that causes gradual but irreversible loss of brain cells. It currently affects an estimated 4.5 million Americans, with the vast majority of sufferers being 65 and older. The group of people who are at the highest risk for Alzheimer’s Disease are those age 85 and older, also the fastest growing segment of the population.</p>
    <p>All statistics are taken from Older Americans 2008: Key Indicators of Well-Being, published by the Federal Interagency Forum on Age Related Statistics. Search the Web for this specific report.</p>
    <p class="subheader">Emotional and Personality Changes</p>
    <p>As we get older, our more dominant personality traits become more pronounced. You may notice that some older people whom you know become “more difficult” to get along with while others become “more mellow” as they age. Some older people may become more concerned about death. Others may feel that they have reached a certain age that their “wisdom” should be respected by everyone and they take offense if someone disagrees or challenges them on an issue.</p>
    <p>As a caregiver, being aware and understanding of some of these changes is important. Showing empathy and compassion to older adults when they may be coping with multiple  changes or losses will benefit both the older person and you as caregiver.</p>
    <p>Emotional, behavioral, and mental change may include depression, memory lapses, inability ot concentrate, or disorientation. It is important to remember that memory lapses are not automatically a sign of dementia or Alzheimer’s Disease! These changes may be caused by a number of factors including:</p>
    <ul>
      <li>Thickening of artery walls </li>
      <li>Poor nutrition </li>
      <li>Action of medications </li>
      <li>Loss of a spouse or death among friends/relatives </li>
      <li>Reduced physical strength and endurance </li>
      <li>Fears of illness </li>
      <li>Major life changes such as moving from one’s home to another setting</li>
    </ul>
    <p>If the change is sudden and impacts the ability of the older adult to function on a daily basis, it is important to consult with the physician as there may be an underlying cause that could be addressed by medical care.</p>
    <p class="subheader">Memory Changes</p>
    <p>A major concern of people as they or their loved ones age is “What is normal memory loss and what is abnormal?”</p>
    <p>By the time a person reaches 70 to 80 years of age, the brain loses 10% of its original size. Some people’s memory remains sharp throughout old age while others are mildly affected by memory loss.</p>
    <p>As many as 50% of older adults have trouble remembering things, a situation called benign forgetfulness. Some loss of short-term memory is common with older adults, yet they often can easily remember life events and experiences from the past. It is also common that older adults tell and retell information. This is not so much a situation that the older adult does not remember telling the story, but it is a way to “lock in” the story into their short-term memory “bank.”</p>
    <p>As the caregiver, rather than responding in an aggravated tone, “Mom, don’t you remember you just told me that story!”, it is better to just listen and then engage mom in more discussion about that story – maybe ask some questions about the situation or relate it back to a past similar experience. It will help mom “bank” that information, as well as you may learn something you did not know about your mom’s life experiences from the past!</p>
    <p class="subheader">Other Changes</p>
    <p>Because it takes longer for the older brain to process information, it takes longer for an older person to react. The performance of routine tasks takes longer. Combined with normal slowing of movement, ordinary tasks may become more difficult and frustrating for both the older person and their caregiver.</p>
