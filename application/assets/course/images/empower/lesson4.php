    <p class="subheader">Realities of Aging</p>
    <p>So, if the stereotype of older adults as slow, sick, and/or senile is false, what is the reality of aging in America? The National Council on the Aging and AARP published the resource, American Perceptions of Aging in the 21st Century, a longitudinal study that began in 1974. In 2002 a follow-up study compared results from the original 1974 study with contemporary findings. Here are some highlights about the realities of aging:</p>
    <ul>
      <li>58% of older adults were very happy with growing older </li>
      <li>88% felt that social relationships were very important to a meaningful, vital life </li>
      <li>32% felt that new learning was very important to a meaningful, vital life </li>
      <li>47% felt their overall health was excellent to very good </li>
      <li>60% were very or somewhat worried about memory loss as they aged</li>
    </ul>
    <p>How about a few more realities of aging. A survey of key trends in aging conducted by <a href="http://www.matherlifeways.com/re_researchandeducation.asp" target="_blank">Mather  LifeWays Institute on Aging</a> documented the following facts about older adults:</p>
    <ul>
      <li>The educational level of the older adult population is increasing. </li>
      <li>Almost half of older adults currently do volunteer work. </li>
      <li>More than 75% of older adults say old age should be defined by a decline in physical or mental functioning rather than a specific age. </li>
      <li>Only 8% of older adults say they are very old. </li>
      <li>Almost 40% of older adults work part-time. </li>
      <li>Older Americans age 65+ comprise 16.3% of the US labor force. </li>
    </ul>
