  <h1>Aging Well</h1>
  <div class="content">
    <p class="subheader">Encouraging Health and Wellness</p>
    <p>Wellness means striving to achieve the optimum state of health and well-being that you are capable of achieving. The whole-person wellness model, which emphasizes personal choice, self-responsibility, optimism, and self-direction comprises six dimensions of wellness: </p>
    <ul>
      <li>Physical </li>
      <li>Social </li>
      <li>Emotional </li>
      <li>Spiritual </li>
      <li>Vocational </li>
      <li>Intellectual</li>
    </ul>
    <p>Physical Wellness includes living an active lifestyle, participating in regular physical activity, eating nutritious foods, and practicing proper self-care. Some ideas for maintaining physical wellness?</p>
    <ul>
      <li>Getting regular physical exams, including dental and eye </li>
      <li>Maintaining current immunizations </li>
      <li>Eating balanced, nutritious meals </li>
      <li>Getting regular exercise </li>
      <li>Joining a health club </li>
      <li>Limiting or avoiding alcohol and tobacco</li>
    </ul>
    <p>Can you think of other ways to maintain physical wellness? How can you help older adult friends or relatives incorporate physical wellness into their lives?</p>
    <p>Social Wellness is promoted through creating and maintaining relationships with others through talking and sharing interests. What are ways to promote social wellness?</p>
    <ul>
      <li>Church groups </li>
      <li>Neighborhood networks </li>
      <li>Meals, gatherings, or outings with friends </li>
      <li>Clubs to expand your social network </li>
      <li>Classes to meet people with similar interests </li>
      <li>Volunteering</li>
    </ul>
    <p>Do you know of activities to promote social wellness that currently exist in your community? Are there ways to promote participation by older friends/relatives?</p>
    <p>Emotional Wellness refers to successfully understanding, managing, and expressing personal feelings. Here are some ways to foster emotional wellness:</p>
    <ul>
      <li>Recognize that your emotions affect your body </li>
      <li>Express needs, feelings, and opinions appropriately </li>
      <li>Use humor to lighten negative thoughts or situations </li>
      <li>Keep a diary to express your thoughts and how they made you feel </li>
      <li>Talk to trusted friends or family members and practice open communication to relieve stress </li>
      <li>Build self-esteem by respecting and taking care of yourself</li>
    </ul>
    <p>What are some ways to help older friends/relatives cope with change and improve emotional wellness? What are some ways you may better manage your own emotional wellness?</p>
    <p>Spiritual Wellness builds on personal values to find meaning and purpose in life and discover a peaceful harmony with the world. While faith means different things to different people, spiritual wellness means having hope, guidance, and purpose to help us understand ourselves better. Some methods of achieving spiritual wellness include:</p>
    <ul>
      <li>Embracing personal beliefs, morals, and/or religion </li>
      <li>Develop connections with ourselves and others </li>
      <li>Develop a philosophy of life </li>
      <li>Explore teachings that appeal to you </li>
      <li>Practice relaxation techniques such as meditation or yoga</li>
    </ul>
    <p>What are some ways you can explore spiritual wellness?</p>
    <p>Vocational Wellness encompasses experiencing personal growth and satisfaction from service to others and learning or improving skills through occupational and other life experiences. While most people equate vocation with paid work, there are many options for pursuing vocational wellness other than employment. How about:</p>
    <ul>
      <li>Volunteering </li>
      <li>Mentoring </li>
      <li>Babysitting </li>
      <li>Caregiving </li>
      <li>Resident Committees </li>
      <li>Seasonal work (e.g., tax preparation) </li>
    </ul>
    <p>What are some other ways you can use your own skills to pursue vocational wellness? What about for older friends/relatives?</p>
    <p>Intellectual Wellness promotes expansion of personal knowledge through learning opportunities, creative pursuits, and stimulating discussions with others. There are numerous ways of pursuing intellectual wellness, many of which may intersect with other wellness categories. How about:</p>
    <ul>
      <li>Joining clubs that explore areas of interest to you </li>
      <li>Taking classes on subjects that interest you</li>
      <li>Pursuing creative hobbies </li>
      <li>Attending plays or musical performances </li>
      <li>Reading books, newspapers, magazines </li>
      <li>Participating in “mental aerobics” (art, puzzles, sensory stimulation techniques) to strengthen, improve, and maintain mental and memory capabilities</li>
    </ul>
    <p>You can probably think of many other ways to promote intellectual wellness. How can you implement those ideas to stimulate intellectual wellness for your older friends/relatives?</p>
    <p class="subheader">Lifelong Learning</p>
    <p>New learning by older adults should be linked to experiences or activities they are familiar with or have enjoyed, particularly those with a social component. Here are some key facts about older adults and lifelong learning:</p>
    <ul>
      <li>Older people can learn new information, and new skills.<br>
      </li>
      <li>Sometimes older people feel less confidence in learning new things, but often this is because they haven’t been in a classroom in a long time, or because they are aware of the stereotype and fall into a self-fulfilling prophesy.<br>
      </li>
      <li>Perhaps the best-documented loss in aging is that it takes longer to understand and think about what is being learned. This just means that learning situations for older people should be self-paced without time pressure.<br>
      </li>
      <li>For older learners, active, participatory learning may be more beneficial (and enjoyable) than traditional, lecture-based learning.<br>
      </li>
      <li>Older learners already know more than when they were young. This can be an advantage if they are given the opportunity to reflect on new information to see how it integrates with what they already know.<br>
      </li>
      <li>A supportive learning environment, with positive feedback and encouragement, is beneficial, provided that older learners are treated like adults.</li>
    </ul>
    <p>How can you apply these lifelong learning strategies to foster wellness?</p>
    <p class="subheader">Creativity</p>
    <p>Creativity is defined as the skill of producing a work of thought or imagination.</p>
    <p>In his book, The Creative Age: Awakening Human Potential in the Second Half of Life (2000), Gene D. Cohen, MD, PhD, provided striking evidence that the human potential for creativity continues well into old age.</p>
    <p>So, what are some ways to encourage creativity among older friends/relatives? Here are some excerpts from a creative energy workshop.</p>
    <p>You may want to try out this workshop with your older friends, relatives, or even yourself. Sometimes, we all need to revisit our creative side!</p>
    <p>Begin by thinking about your interests and desires. This may include goals you want to achieve, projects you want to implement, or dreams you want to make real. By going through this exercise, you will begin to realize inner strengths you already have that will help you achieve your potential.</p>
    <ol>
      <li>Do you wish to build on what you have already done, or do you wish to take your work to a higher level?
        <ul>
          <li>What do you need to do to grow?</li>
          <li>Do you know others who do related work and whose achievements you want to  mirror? They may be agreeable to become your coach or mentor!</li>
          <li>Search for new resources that will help you achieve your goals. Network with others  who may be of help to you.</li>
          <li>Look for inspiration around you. Seek out success stories.</li>
        </ul>
      </li>
      <li>Do you want to change direction in your work or activities?
        <ul>
          <li>Look to strengths or interests you have that are untapped.<br>
          </li>
          <li>Start exploring the new area. You do not need to make a commitment yet.<br>
          </li>
          <li>Experiment just as if you were vacationing to a new area or testing new recipes.<br>
          </li>
          <li>Empower yourself. Be confident that you can learn and growth in a new area.<br>
          </li>
          <li>Guess what – you are not the first to make a change. If others can, so can you!</li>
        </ul>
      </li>
      <li>Time to test your capacity for starting creativity.
        <ul>
          <li>Continuously putting off something new needs to end.<br>
          </li>
          <li>Look at ordinary experiences as new opportunities for self-discovery.</li>
        </ul>
      </li>
      <li>Consider collaborative creativity.
        <ul>
          <li>Look for others who may be interested in starting a new project with you.<br>
          </li>
          <li>Consider volunteering in your community for a project as a way to meet new contacts and expand your experiences.</li>
        </ul>
      </li>
      <li>Is your circle of friends intergenerational?
        <ul>
          <li>Look at ways to bring intergenerational activities into your life. You will be surprised at the variety of perceptions about the same topics.<br>
          </li>
          <li>Spend more time with your older relatives and friends to learn about their history. Along with strengthening family ties, you may learn something new about your older  relatives and friends!</li>
        </ul>
      </li>
      <li>Are you seeking a sense of personal satisfaction or purpose?
        <ul>
          <li>Recognize the power of small changes.<br>
          </li>
          <li>Purposively change your language to “positive speak”. For example, a “problem” can become an “opportunity” or “challenge”. You will notice that your positive tone will gradually influence others in the same directions!</li>
        </ul>
      </li>
      <li>Do you want to focus on public creativity? </li>
      <ul>
        <li>Look for ways you may become a volunteer or activist for your community.</li>
        <li>Consider that many of your skills or knowledge are already valuable assets to your community. Seek ways to share those skills or knowledge.</li>
      </ul>
      <li>Are you now asking, “Am I really creative enough?”
        <ul>
          <li>Everyone has more potential than they know.<br>
          </li>
          <li>Creativity does not need to be something grand or dramatic. </li>
          <li>Creativity takes many forms and can be as simple as a pleasant social interaction where two friends explore something new about each other or their mutual interests</li>
        </ul>
    </ol>
