  <h1>Focusing on Healthy Aging</h1>
  <div class="content">
    <p>First of all, how do we define healthy aging? <a href="http://www.ncoa.org/" target="_blank">The National Council on the Aging</a> gives this definition:</p>
    <p>“Healthy aging is a broad concept that is more than just physical health status or absence of disease. It encompasses all of the intellectual, emotional, social, and spiritual facts of our being. While healthy aging is driven by internal dimensions such as our beliefs, attitudes, and intentions about our health, it also depends on external supports including our social networks, community services, public policies, and the built and natural environment. These dimensions are interrelated, affecting behavior and lifestyle choices.”</p>
    <p class="subheader">Maintaining Physical Health</p>
    <p>Research shows that older adults can attain healthy vital old age through simple health promotion and disease prevention initiatives. The MacArthur Foundation study identified several lifestyle changes that clearly proved to benefit older adults:</p>
    <ul>
      <li>Exercise and physical activity </li>
      <li>Early detection of cancer </li>
      <li>Reducing risk of heart disease </li>
      <li>Vaccinations</li>
    </ul>
    <p>Exercise and physical activity<br>
      Exercise can help prevent heart disease, high blood pressure, and tendency toward diabetes, osteoporosis, and risk for falls. A regular, moderate program of aerobic and strength training for older adults is both safe and effective in improving physical function.</p>
    <p>Early detection of cancer<br>
      With early detection, many forms of cancer are treatable and have a high survival rate. Fortunately, screening tests for early detection of cancer have become very effective and commonly available. In addition, research suggests that promoting a healthy diet may also decrease the risk of certain types of cancer.</p>
    <p>Heart disease<br>
      Heart disease is a major killer of men at all ages and of older women. Education about reducing risk factors (high cholesterol, smoking, hypertension) is vitally important for older adults.</p>
    <p>Vaccinations<br>
      Among the greatest advances in health promotion and disease prevention in older adults had been the development of safe and effective vaccines. Among non-vaccinated older adults, disease can sweep through the population rapidly and can be very dangerous.</p>
    <p class="subheader">Maintaining High Cognitive and Physical Function</p>
    <p>Fear of cognitive loss, and especially of Alzheimer’s disease are widespread among older adults, and with good reason. Such losses place great burden on both the older adult and his/ her caregivers. Fortunately, research shows that even though the proportion of older adults in the population is increasing, the usual pattern is that most older adults retain a good portion of their independence and mental sharpness.</p>
    <p><a href="../empower/www.macfound.org" target="_blank">The MacArthur Foundation</a> study documented a number of ways that older adults sustain mental ability as they age. Many of the study’s “successful agers” reported that such activities as reading, conversation, crosswords and other word puzzles, card games, and other similar activities kept their minds alert and active. The study also shows that the strongest predictors of sustained cognitive function include:</p>
    <ul>
      <li>Education </li>
      <li>Physical activity </li>
      <li>High “self-efficacy”</li>
    </ul>
    <p>Education<br>
      People with more years of education were more likely to maintain high cognitive function. The study that education early in life may have a direct beneficial effect of brain circuitry, and education may set a pattern of intellectual pursuits, including reading and puzzle solving, which maintains lifelong exercise of cognitive function.</p>
    <p>Physical Activity<br>
    The study found that older adults who engaged in strenuous physical activity were much more likely to maintain high cognitive function. One possible answer for this is that exercise releases chemicals in the brain which promote the growth of new brain cells.</p>
    <p>Self-efficacy<br>
      Self-efficacy, or self-esteem, is a person’s belief in his or her ability to handle various situations. Many studies show that a strong sense of self-efficacy leads to improved performance in solving cognitive problems. Older adults with strong self-efficacy are more likely to view memory as a set of skills that can be learned and improved.</p>
    <p class="subheader">Engagement in Productive Relationships and Behaviors</p>
    <p>The MacArthur research showed that “happy activities” are essential to successful aging. The two most important areas they identified that contribute to successful aging are:</p>
    <ul>
      <li>Relating to others </li>
      <li>Continuing productive activity.</li>
    </ul>
    <p>Relating to Others
      Being part of a social network of family and friends contributes significantly to successful aging.</p>
    <p>Continuing Productive Activity
      Most people equate being productive with earning money. However, there are many activities, both paid and unpaid, which can be considered productive.</p>
