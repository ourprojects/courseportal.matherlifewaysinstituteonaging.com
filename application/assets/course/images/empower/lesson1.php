  <h1>Managing Self-Care</h1>
  <div class="content">
    <p>Managing our self-care means that as caregivers we:</p>
    <ul>
      <li>Take responsibility - We realize we are responsible for our personal well-being and for getting our needs met. This includes maintaining activities and relationships that are meaningful to us.</li>
      <li>Have realistic expectations - We fully understand our family member's medical condition and we are realistic about what our family member can and cannot do. The more you know about your family member's medical condition, the better you will be able to plan successful caregiving
        strategies. Knowledge is power. It's also important to look at your definition of a <em>good caregiver</em>. Unrealistic expectations can set you up for feelings of failure, resentment, and guilt. Placing burdensome expectations on yourself does not make you a better caregiver. In fact, you are much more likely to become an exhausted, irritable, and resentful caregiver... and then to feel guilty!</li>
      <li>Focus on what we can do - It's important to be clear about what you can and cannot change. For example, you will not be able to change a person who has always been demanding and inflexible, but you can control how you respond to that person's demands. You can <em>accept</em> and <em>let-go</em> of-the things you cannot change. Managing your self-care also means you seek solutions to what you can change.</li>
      <li> Communicate effectively with others - These include family members, friends, health care professionals, and the care receiver. Don't expect others to know what you need. Recognize it is your responsibility to tell others about your needs and concerns. Communicate in ways that are positive and avoid being demanding, manipulative, or guilt provoking when you make requests.</li>
      <li> Learn from our emotions - Realize there will be emotional ups and downs. Listen to your emotions and what they are telling you. Don't bottle up your emotions. Repressing or denying feelings decreases energy, causes irritability, depression, and physical problems, and affects your judgment and ability to make the best decisions. Also, don't strike out at others. You are in control of your emotions, your emotions don't control you.</li>
      <li>Get help when needed - An important part of self-care is knowing when you need help and how to find it. Help can be from community resources, family and friends, or professionals. Most important is that you do not wait until you are <em>hanging at the end of your rope</em> before you get help. Don't wait until you are overwhelmed or exhausted, or your health fails. Reaching out for help, when needed, is a sign of personal strength.</li>
      <li>Set goals and work toward them - Be realistic in the goals that you set and take steps toward reaching those goals. Seek solutions to the problems that you experience. Changes do not need to be major to make a significant difference. In summary, self-care means that you seek ways to take better care of yourself. As a caregiver, you don't just survive. You thrive!</li>
    </ul>
