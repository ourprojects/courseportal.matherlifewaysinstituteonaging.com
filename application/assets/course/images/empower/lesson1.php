  <h1>Caregiver Resources</h1>
  <div class="content">
    <p>Too often caregivers neglect their own health and well-being, and put their own needs <em>on the back burner</em>. Sometimes caregivers become a second victim of the disease that afflicts their family member. It's sad when someone says, <em>My mother was the ill person, but her illness destroyed my father</em>. Usually, we cannot stop the impact of a chronic illness on a family member. However, we are responsible for our own       self-care.</p>
    <p>When you board an airplane, the flight attendant gives several safety instructions. One of them is, <em>If oxygen masks drop down, put on your oxygen mask first before helping others. </em> This is because if you don't take care of yourself first, you may not be able to help those who need your help. It's the same thing with caregiving. When you take care of yourself, everyone benefits. Ignoring your own needs is not only potentially
      detrimental to you, but it can also be harmful to the person who depends on you.</p>
    <p>The <em>Resource</em> section was designed to give you additional details in order to help you maintain personal well-being while providing quality care to your family member. Many focus on  tools to help you to take care of you. These tools will help you</p>
    <ul>
      <li>set goals and make action plans.</li>
      <li> identify and reduce personal stress.</li>
      <li> make your thoughts and feelings work for you, not against you.</li>
      <li> communicate your feelings, needs, and concerns in positive ways.</li>
      <li> cope with difficult situations, including asking for help and setting limits.</li>
      <li> deal with emotions, especially feelings of anger, guilt, and depression.</li>
      <li> make tough caregiving decisions.</li>
    </ul>
    <p>Additional resources will address special concerns and decisions you may face as a caregiver. These include what to do when a family member is no longer a safe driver, hiring in-home help, using community services, how to communicate with and respond to a family member who is memory impaired, options available when a family member is having problems managing his money; coping with depression, and making a decision about a care
      facility. You can turn to resources for guidance and direction when you face a specific decision or concern.</p>
