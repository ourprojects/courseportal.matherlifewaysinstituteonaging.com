  <h1>Making Action Plans</h1>
  <div class="content">
    <p>An action plan is a specific action that you are confident you can accomplish within the next week. It's an agreement or contract with yourself.</p>
    <p>Action plans are one of your most important self-care tools. An action. plan is a step toward reaching your long-term goal. It is to be something you want to do. It is not to be something you feel you should do, have to do, or need to do. The intent of making an action plan is to help you to feel better and to take better care of yourself. Remember, an action plan is a "want to do." Here are the five steps for making an action plan: </p>
    <ol>
      <li>Decide what you want to do.</li>
      <li> Make your plan behavior-specific.</li>
      <li> Make a specific plan. </li>
      <li>Determine your confidence level. </li>
      <li>Write down your action plan. </li>
    </ol>
    <p class="subheader">Decide What You Want To Do</p>
    <p> Think about what is realistic for you to accomplish within the next week. It's important that an action plan is reachable; otherwise, you are likely to experience frustration. An action plan is to help you experience success-not frustration, increased stress, or failure. An action plan starts with the words, "I will ... " If you find yourself saying "I will try to ... ," "I have to ... ," or "I should ... ," then re-examine your action plan. It probably is not something that you truly want to do.</p>
    <p class="subheader">Make Your Plan Behavior-Specific</p>
    <p>The more specific your action plan, the greater your chances of accomplishing it. For example, "taking better care of myself" is not a specific behavior. However, making an appointment for a physical check-up, walking three times a week, getting a massage on Thursday afternoon, or asking
      someone to stay with your family member for one morning are all specific behaviors. "I will relax" also is not a specific behavior; however, reading a book, listening to your favorite music, or puttering in the garden are specific behaviors.</p>
    <p class="subheader">Make a Specific Plan</p>
    <p>Making a specific plan is often difficult, yet it is the most important part of making an action plan. A specific plan answers these four questions:</p>
    <ol>
      <li>What are you going to do? - Examples: I will read (book name) for pleasure. Or, I will walk.</li>
      <li>How much will you do? - Examples: Will you read one chapter or will you read for a half hour? Will you walk two blocks or for 20 minutes?</li>
      <li>When will you do this? Examples: Will you read the first thing in the morning when you awaken, before you go to bed, when the care receiver is sleeping, or ... ? If your plan is to walk, when during the day will you do it ?</li>
      <li> How often will you do this activity? Example: Three times a week on Monday, Wednesday, and Friday.</li>
    </ol>
    <p>A common mistake is to make an action plan that is unreachable within the time frame. For example, if you plan to do something every day, you might fail. Caregiving, and life in general, has its surprises. Although well-intentioned, it's often not possible to do something every day. It's better to plan to do something once or twice a week and exceed your action plan than to plan to do something every day and fail because you only did it six days, rather than seven. Remember, an action plan is meant to help you to take better care of yourself and to experience success. The last thing you need is additional pressure, disappointment, and stress.</p>
    <p>Here are two recommendations for writing an action plan that can help you achieve success.</p>
    <ol>
      <li>Start where you are or start slowly. If there is a book you have been wanting to read, but just have not found the time, it may not be realistic to expect to read the entire book in the next week. Instead, try reading for a half hour twice during the week If you haven't been physically active, it may be unrealistic to make an action plan· to start walking three miles. It is better to make your action plan for something that you believe you can accomplish. For example, make your plan for walking three blocks or a half mile, rather than three miles.</li>
      <li>Give yourself time off. We all have days when we don't feel like doing anything. That's the advantage of saying you will do something three days a week, rather than every day. That way, if you don't feel like doing something on one day, or something develops that prevents you from doing it, you can still achieve your action plan.</li>
    </ol>
    <p class="subheader">Determine Your Confidence Level</p>
    <p>Once you have made your action plan, ask yourself the following question: On a scale of 0 to 10, with 0 being not at all confident and 10 being totally confident, how confident am I that I can complete my action plan? If your answer is 7 or above, your action plan is probably realistic and reachable. However, if your answer is 6 or below, it's important to take another look at your action plan. Something probably needs to be adjusted.
    <p>Ask yourself: <em>What makes me uncertain about accomplishing my action plan? What problems do I foresee?</em> Then, see if you either find a solution to the problems you identified or change your action plan to a new one in which you feel greater confidence.
    <p class="subheader">Write Down Your Action Plan
    <p>Once you are satisfied with your action plan, write it down. Putting an action plan in writing helps us to remember, keep track of, and accomplish the agreement we have made with ourselves. Keep track of how you are doing. Write down the problems you encounter in carrying out your action plan. Check off activities as you accomplish them. If you made an adjustment in your action plan, make a note of what you did.
    <p>At the end of the week, review your action plan. Ask yourself, "Am l nearer to accomplishing my goal?" "How do I feel about what I did?" What obstacles or problems, if any, did I encounter?" Taking stock is important. If you are having problems, this is the time to seek solutions.</p>
  </div>
  <h1>Problem-Solving: A Solution-Seeking Approach</h1>
  <div class="content">
    <p>Sometimes you may find that your action plan is not workable. You may encounter unusual circumstances that week and need to give the plan a try for at least another week or you may need to make adjustments in your original plan. The following solution seeking approach can help you identify solutions to problems.</p>
    <ul>
      <li>Clearly identify the problem. This is the first and most important step in the solution-seeking approach. It also can be the most difficult step. </li>
      <li>List ideas to solve the problem. Family, friends, and others may be helpful in giving ideas. When you ask for ideas, just listen to each suggestion. It's best not to respond as to why an idea is or isn't likely to work. Just focus on getting the ideas.</li>
      <li> Select one to try. When trying a new idea, give it a fair trial before deciding that it won't work.</li>
      <li> Assess the results. Ask yourself, "How well did what I chose work?" If all went well, congratulate yourself for finding a solution to the identified problem. If the first idea didn't work, try another idea. Sometimes an idea just needs fine-tuning. It's important not to give up on an idea just because it didn't work the first time. If you have difficulty finding a solution that works, utilize other resources. Share your problem with family, friends, and professionals and ask them for possible ideas. If you still find that suggested solutions do not work, you may need to accept that the problem is not solvable right now.</li>
    </ul>
    <p>Remember, just because there  doesn't seem to be a workable solution right now doesn't mean that a problem can't be solved later, or that
      other problems can't be solved in the same way. It may be helpful to go back to the first step and consider if the problem needs to be redefined. For example, a caregiver had thought that her problem was "I am tired all of the time." However, the real problem was the caregiver's beliefs that "No one can care for John like I can," and "I have to do everything myself." As a result of these beliefs, the caregiver was doing everything herself and getting worn out. When she redefined the problem and focused on changing her beliefs and view of the caregiving situation, she found a workable solution. Sometimes, too, a problem may be easier to work on if you break it down into smaller problems.</p>
    <p>Most of the time if you follow these steps, you will find a solution that solves the problem. It's important to avoid making the mistake of jumping from step l to step 7 and thinking "nothing can be done."</p>
  </div>
  <h1>Reward Yourself</h1>
  <div class="content">
    <p>Accomplishing action plans is often a reward in itself. However, it's also important to find healthy pleasures that add enjoyment to your life. Rewards don't have to be fancy or expensive or take a lot of time. One caregiver; for example, regularly goes to a movie or a play as a gift to herself from her husband. She said:</p>
    <p>When my husband was well, he would take me out Friday nights to a movie or a play at least twice a month. Because of his medical condition, he is no longer able to do so. Now a friend and I go to a movie or a play at least once a month. I consider this is a treat that my husband is still
      giving to me.</p>
    <p>Another caregiver said:</p>
    <p>Before my wife's illness, I would go golfing with my buddies on Saturday morning. When Carmela needed more care, I quit golfing. I now treat myself to Saturday golfing, while my daughter or a friend visits with Carmela. This gives me something to look forward to each week and I feel more alive when I return home. I'm also finding I am more patient with Carmela. My daughter says I am always happier and calmer when I return home.
      So, I look at Saturday golfing as my treat not only to me, but also to Carmela.</p>
  </div>
  <h1>My Action Plan</h1>
  <div class="content">
    <p> In review, a caregiver who practices selfcare does the following:</p>
    <ol>
      <li>Sets goals.</li>
      <li>Identifies a variety of options for reaching a goal</li>
      <li>Makes an action plan toward accomplishing the goal.</li>
      <li> Carries out the action plan.</li>
      <li> Assesses how well the action plan is working.</li>
      <li> Makes adjustments, as necessary, in the action plan.</li>
      <li>Rewards himself or herself.</li>
    </ol>
    <p>Not all goals are achievable. Sometimes we must accept that what we want to do is not possible at this time, and we must let go of the idea. Be realistic about goals and don't dwell on what can't be done.</p>
    <p>Consider what is likely to happen to the caregiver who is driven by a goal to make her mother happy. Given her mother's personality, this goal may be completely unachievable. Such a goal creates a heavy burden and a caregiver is not likely to achieve it. However, an achievable goal       might be to provide a pleasurable activity for her mother at least once a week perhaps taking her to get her hair done, visiting a friend, watching a comedy on television, or working together on a project her mother enjoys.</p>
    <p>Remember, what is important in caregiving is not just to survive, but to thrive!</p>
