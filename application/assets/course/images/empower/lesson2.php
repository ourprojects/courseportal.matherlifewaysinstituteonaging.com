    <p>Practicing self-care -</p>
    <p>To be an effective caregiver you need to
      maintain your own health and spirit, and
      to nurture yourself. All too often caregivers
      put their own needs last. Studies show
      that sacrificing yourself in giving care to
      another can lead to emotional exhaustion,
      depression, and illness.</p>
    <p>Maintaining your health and spirit can
      reduce your level of stress. It is critical to
      find activities that help you to stay healthy
      and nurture yourself. These activities are
      different for each individual. What works
      for one person may not work for another.
      You must find stress-reducing methods that
      work best for you.</p>
    <p>We can learn a lot from a self-care
      program in Florida called "Getting Well."
      This is a group of people who are supporting
      each other in learning to live and feel better.
      They take part in life-affirming activities
      such as "laughing, juggling, playing, meditating,
      painting, journal writing, exercising,
      and eating nutritiously" They demonstrate
      the necessity of associating with others who
      help you maintain your spirit and help you
      feel loved and supported.
      To manage stress, it is essential to take
      breaks from caregiving. Plan them into your
      schedule, starting immediately; if you have
      not done so already. Studies show that caregivers
      often don't take breaks until they are
      at the "end of their rope" or "burned out."</p>
    <p>This serves no one's best interest as your
      ability to function can be seriously affected.
      To avoid problems, it is your responsibility
      to take time off from caregiving to refresh
      yourself.
      It is important to the well-being of care
      receivers that you take breaks. If you don't,
      they may become increasingly dependent
      on you. If you take breaks, they will be less
      isolated and will benefit from having contact
      with other people. They also need breaks
      from you. (This is an example of reframing
      your perception of a situation.)</p>
    <p>You are responsible for your own self-care.
      Practicing self-care means that you: </p>
    <ul>
      <li> learn and use stress reduction techniques. </li>
      <li> attend to your own health care needs. </li>
      <li> get proper rest and nutrition. </li>
      <li> exercise regularly </li>
      <li> take time off without feeling guilty. </li>
      <li> participate in pleasant, nurturing
        activities. </li>
      <li> reward yourself.. </li>
      <li> seek and accept the support of others. </li>
      <li> seek supportive counseling when you
        need to, or talk with a trusted counselor,
        religious advisor, or friend. </li>
      <li> identify and acknowledge your feelings. </li>
      <li> tell others what you need. Don't assume
        "they should know." </li>
      <li> change the negative ways you view
        situations.</li>
      <li> set goals and prioritize.</li>
    </ul>
