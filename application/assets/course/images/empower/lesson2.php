    <p class="subheader">Step 2: Identify Your Sources of Caregiving Stress -</p>
    <p>The second step in managing stress is to recognize what causes your stress. Not all stressors are the result of caregiving. Other sources can affect your ability to be a caregiver. The following questions include many common sources of stress. Thinking about these questions can help you recognize some of your own sources. </p>
    <p class="blog">Are you experiencing many demands on your time, energy, or money? What are they?</p>
    <p class="blog"> Do you feel you have conflicting responsibilities? Which ones?</p>
    <p class="blog">Are there differences in expectations between your family, your boss, the care receiver, and yourself? What are
      they?</p>
    <p class="blog">Do you feel others don't understand the care receiver's mental or physical condition?</p>
    <p class="blog"> Do you have difficulty meeting the care receiver's physical or emotional needs?</p>
    <p class="blog">Are you pressured by financial decisions and lack of resources?</p>
    <p class="blog">Do you feel a loss of freedom, to the point of feeling trapped?</p>
    <p class="blog"> Is there disagreement among family members?</p>
    <p class="blog"> Do you feel that other family members aren't doing their share?</p>
    <p class="blog">Does the care receiver place unrealistic demands and expectations on you?</p>
    <p class="blog">Is there a lack of open communication between you and the care receiver?</p>
    <p class="blog">Do other family members have negative attitudes that create difficulty for you?</p>
    <p class="blog">Is it painful to watch the care receiver's condition get worse?</p>
    <p class="blog">Are there other problems with children, marriage, job, finances, or
      health? What are they?</p>
    <p>Consider your "yes" answers carefully. The sources of stress you have identified are indicators for change. Use the awareness you have gained in the first two steps to make helpful changes.</p>
    <p>The following story is an example of a caregiver who recognized the source of her distress and made changes to better manage the situation.</p>
    <p>Ernestine was increasingly fatigued, irritable, and depressed with the responsibility of caring for her husband, Richard, who had Parkinson's disease. Richard's condition was steadily getting worse. He
      was bed-bound and needed help with many
      functions. Other family members hadn't
      offered to help, and Ernestine felt abandoned,
      alone, angry, and overwhelmed.
      A few friends and neighbors had offered
      to help but Ernestine refused. When she
      started having health problems, it became
      clear that something had to change. She
      had to have help.</p>
    <p>Because Ernestine had difficulty asking for
      help, she devised a simple plan that would
      give others an opportunity to help without
      having to be asked. She made a list of tasks
      she needed help with and posted it on the refrigerator. The list included such things as vacuuming the living room, grocery shopping, staying with Richard so she could go to church, weeding the garden, picking up audio books at the library, picking up medications at the pharmacy,
      and preparing food. When visitors offered
      to help, Ernestine referred them to the list,
      suggesting they choose a task that suited
      them. This proved to be a successful plan
      for everyone.</p>
    <p>It's important to identify the causes of
      your stress before they overwhelm you.
      Don't wait until you develop health
      problems, as Ernestine did. Many caregivers
      keep going until they become ill. You can
      only be an effective caregiver if you are
      healthy. Self-sacrifice to the point of illness
      benefits no one and is not required or
      recommended.</p>
    <p class="subheader">Step 3: Identify What You Can and
      Cannot Change -</p>
    <p>A major challenge of caregiving is to not only survive, but to rebuild your life and thrive. This is possible once you know the sources and signs of your stress. Then you can determine those you can do something about and those that are beyond your control. Step three is to identify what you can and cannot change </p>
    <p>Identifying what you can change gives you a sense of control over events. However, it isn't easy to determine what can and cannot be changed. Too often people try to change things they have no control over. For example, someone who focuses on trying to change another person usually ends up more frustrated. The only person
      you can change is yourself. You may be able
      to change a situation, how you respond to
      it, or your perception of it, but you can't
      change another person. It wastes valuable
      time and energy trying to change what is
      outside of your control. Some situations can't be changed.
      However, you may be able to manage them
      better if you change your outlook about
      a situation, or decide to 'roll with the
      punches.'</p>
    <p>The frustration and hopelessness that
      result from trying to change the unchangeable are self-defeating and can adversely
      affect a relationship, as in the case of Hal
      and Sue.</p>
    <p>Sue and Hal had been a socially active
      couple. Sue was diagnosed with early
      Parkinson's disease and gradually started
      backing out of social plans because she
      didn't feel up to it. Since the beginning of
      the disease Sue has been on a roller coaster
      of having good days and bad days. Hal
      encourages Sue to go out when she doesn't
      feel like it, urging her to 'snap out of it.'  
      He wants things to remain as they were.</p>
    <p>Hal is frustrated in his attempts to change
      the effect of the disease on their lives. By
      not accepting Sue's feelings, he is adding
      stress to their relationship. But recently he
      has learned more about Parkinson's disease
      and is trying to be more realistic and
      flexible about what he can and cannot
      change. Flexibility is crucial. A Japanese
      saying is:</p>
    <p>'In a storm, it is the bamboo, the flexible tree,
      that can bend with the wind and survive. The
      rigid tree that resists the wind falls, victim of
      its own insistence on control.'</p>
    <p>Bending with the wind is crucial to
      surviving the winds of change, including
      those involved in caregiving. At times, both
      you and the care receiver may feel a loss
      of control over your lives. While feeling in control is important, sometimes it can
      be me a problem because the more we try
      to control, the less control we seem to have.
      Being flexible can help us keep a positive
      attitude, despite hardships.</p>
    <p>Use the following guidelines to look at
      your situation and to determine what can
      and cannot be changed:</p>
    <ol>
      <li> Accept the reality of your caregiving
        situation. </li>
      <li> Educate yourself about the care receiver's
        disease. </li>
      <li> Identify unrealistic expectations,
        especially your own.</li>
      <li> Seek and accept support. </li>
      <li> Identify what you still have, rather than
        focus on what is lost. </li>
      <li> Let go of what cannot be changed. </li>
    </ol>
    <p class="subheader">Accept the reality
      of your caregiving situation</p>
    <p>When making changes it is necessary, but
      not always easy; to accept reality. We often
      deny things that hurt, and that can keep us
      from seeing a situation as it really is.  Jane heard the doctor tell Joe that he had a
      serious illness. He also told Joe he would
      need more rest and help with certain daily
      activities. Still, Jane found herself feeling
      annoyed when Joe took frequent naps,
      especially since she was taking on more
      responsibility for managing things at home.
      It took time for Jane to stop denying, and
      start accepting, the full impact of the
      disease. It was then that she was able to
      see realistically what could and couldn't
      be changed.</p>
    <p>Jane is coping in a more adaptive way.
      However, Joe's mother denied the seriousness
      of the disease long after Jane came to
      terms with it. Family members may take
      different lengths of time to accept reality,
      which can add to the stress of caregiving.</p>
    <p class="subheader">Educate yourself about
      the care receiver's disease</p>
    <p>You will be better able to identify what
      you can and cannot change when you
      understand the disease. For example, without
      knowledge about the communication
      abilities of someone with Alzheimer's
      disease, you may try to reason with the
      person or expect him to tell someone
      something you consider easy to remember.
      This will probably frustrate both of you.
      There are many sources of information
      about specific diseases, including your
      personal physician, medical libraries, and
      associations related to specific diseases,
      such as Alzheimer's and Parkinson's disease.
      If you have access to a computer that is
      linked to the Internet, you can find a wealth
      of current information on diseases and
      disease-related associations.</p>
    <p class="subheader">Identify unrealistic expectations,
      especially your own</p>
    <p>You can make changes successfully only
      when your expectations are realistic. How
      realistic are yours? Do you often feel
      anxious because you expect more of yourself
      than you can achieve? Many caregivers
      listen only to the 'shoulds' they have
      been raised with. Women, especially, often
      believe they "should" be able to do
      everything themselves, and when that isn't
      possible, they feel guilty or depressed. If
      you have unrealistic expectations of yourself,
      then your expectations of what can be
      changed probably will be unrealistic also.
      The following story is an example of a
      caregiver, Rosa, who with her husband,
      Dean, made constructive changes in what
      was a difficult, stressful situation.</p>
    <p>Rosa was devastated when Dean, her
      husband of 40 years, suffered a sudden,
      severe stroke that left him partially paralyzed
      on one side of his body and unable
      to speak. The stroke was a shock. .Rosa's
      initial response was to become overly
      protective and do everything for Dean.
      She was afraid to leave him alone for fear
      something terrible would happen.
      Before the stroke, Rosa and Dean had been
      making retirement plans, which included
      extensive travel. Those plans were forsaken
      as they both felt increasingly overwhelmed,
      fearful, isolated, and depressed.
      Rosa became extremely fatigued and irritable
      as Dean became increasingly dependent
      on her. The visiting nurse talked with
      them about what Dean could and could
      not do for himself. She emphasized the
      importance of Dean maintaining as much
      independence as possible. It became
      apparent that Dean could do many things
      for himself, including writing letters to
      family and friends. Dean felt better as he
      became more independent. Rosa was able
      to be more realistic in her expectations.
      She realized that Dean's dependence on her
      was detrimental to their relationship.</p>
    <p>As Rosa and Dean gradually adapted to
      living with the stroke, they became less
      fearful and more hopeful. They began
      looking at the quality of their remaining
      life together. They wanted, more than
      anything, to travel together and decided
      to take a short trip to see how it would go.
      The first trip was successful and they felt
      encouraged to travel more. Rosa found a
      travel agent who helped them plan trips
      that accommodated Dean's disabilities.
      They enjoyed several trips before Dean's
      death 12 years later.
      Rosa and Dean responded to this
      challenge by gaining an understanding of
      the disease, accepting reality; setting
      realistic expectations, and changing what
      could be changed.</p>
    <p class="subheader"> Seek and accept support</p>
    <p>Many caregivers find it difficult to ask
      for help. Rosa initially refused help from
      friends and neighbors. She did everything
      herself until she started feeling distressed.
      The expectations she had for herself were
      overwhelming and unrealistic. It wasn't
      until she began seeking support from the
      visiting nurse, travel agent, and others that
      she was able to find a way to make changes.
      Often you can make changes only with the
      help of others. Seeking and accepting
      support may be the single most important
      factor in making constructive changes.</p>
    <p class="subheader">Identify what you still have,
      rather than focus on what is lost</p>
    <p>When Rosa and Dean decided to look
      for "what remained" in their situation, they
      hoped that they still had quality in their life
      together. They looked at what they still had,
      rather than focusing on what had been lost,
      and they made changes that were still
      possible.</p>
    <p>They found an unexpected 'gift' as they
      made changes and adapted to the illness.
      Rosa said, 'I never would have asked for
      the stroke to happen, but it was because of
      it that Dean and I learned what love was all about. I am a different person than I was.
      I am more understanding, patient, caring,
      and sensitive to the pain of others. Many caregivers, as they learn more
      about themselves, experience personal growth.   
      That is the 'gift' that can often be found 
      in difficult times.</p>
    <p class="subheader">Let go of what cannot be changed</p>
    <p>It is natural to want to hold on to things
      as they were. But letting go of what you
      cannot change is accepting the situation as
      it is. It releases you from the need to control
      what you cannot change. Letting go is a way
      to cooperate with the inevitable. It releases
      new energy for accepting reality and seeing
      new possibilities. Sam is a prime example of
      someone who is learning to let go.</p>
    <p>Sam had always been an intense athletic
      competitor, and sports had been the
      driving force in his life. At age 45 he had a
      slight stroke which left him mildly
      affected. Sam's problem wasn't that he had
      a stroke; the problem was that he couldn't
      let go of wishing that he hadn't had one.
      He continuously wanted things to be as
      they had been. This made him feel angry
      and frustrated. Fortunately, Sam reached
      a point of wanting to learn to live with the
      stroke and to let go of wanting life to be as
      it had been before.</p>
    <p>Sam was unable to live in the present
      until he let go of his desire for things to
      be as they were. The "if onlys" and "what
      ifs" were a source of suffering. When Sam
      let go, he learned to live with the stroke
      and made changes that helped him develop
      a satisfying life. What Sam learned also
      applies to caregivers, as shown in the case
      of Marsha and Bud.</p>
    <p>Marsha was the caregiver for her husband,
      Bud, who had Parkinson's disease. Buds
      condition worsened and he and Marsha
      were unable to do any of the things they
      had done in the past. Marsha continually
      wanted things to be the way they had been.
      "If only" became her constant thought: "If
      only Bud could dress himself," "If only we
      could go dancing like we used to," "If only
      Bud had more energy," "If only he could
      still drive us places."
      Marsha's unhappiness caused a strain
      in their relationship. It was only when
      she and Bud were having a good time
      playing cards with friends one day that she
      realized how much valuable time she was
      wasting by constantly wanting things to
      be different. She began to let go of "if only"
      and to accept "what is." In letting go, she
      found acceptance and peace of mind.</p>
    <p class="blog">As you reflect on your challenges as a
      caregiver, consider these questions. What
      can I change?
      What must I
      accept? What
      can I improve?</p>
    <p class="subheader">Step 4: Take Action
      to Manage Your Stress</p>
    <p>The fourth step points the way for you to
      manage and reduce your stress. There are
      many different tools for managing stress.
      But you must find what is most effective
      for you. Proven ways to manage and reduce
      stress include: </p>
    <ul>
      <li>managing your thoughts, beliefs, and
        perceptions. </li>
      <li>practicing self-care. </li>
      <li>getting social support. </li>
      <li> using techniques that lower stress. </li>
      <li>developing plans of action. </li>
      <li>finding hope and meaning. </li>
    </ul>
    <p class="subheader">Managing your thoughts,
      beliefs, and perceptions</p>
    <p>Thoughts and beliefs are the foundation
      of experience. They are not only reactions
      to events but our thoughts and beliefs can
      also influence events. What we think and
      believe affects what happens. Managing our
      thoughts means we have control over how
      we view things. As a caregiver, there may be
      times when the only thing you can change
      is how you view a situation.
      There are several tools for managing
      thoughts, beliefs, and perceptions. Two that
      can be helpful are reframing and self-talk.</p>
    <p>Reframing -</p>
    <p>Your frame of reference is the
      window through which you view the world.
      It gives meaning to your world. You see
      things one way, but someone else sees the
      same circumstances differently. Situations
      become more stressful when you view them
      in a negative way.
      Reframing is learning to look at things in a
      different way, for example, finding something
      positive about a difficult situation. Some
      examples of reframing include:</p>
    <ul>
      <li>A caregiver who views the behavior of
        someone with Alzheimer's disease as
        "purposefully behaving that way to get
        to me" versus taking the view that "the
        behavior is a part of the disease."</li>
      <li> A caregiver who is angry at her brother
        for helping only once a month versus
        taking the view that "any help, no matter
        how little, will lighten my load." </li>
      <li> A caregiver who puts the situation into
        a religious or philosophical framework,
        such as "This is happening because God
        is angry with me" versus taking the view
        that "God is giving me an opportunity to
        learn and grow."</li>
    </ul>
    <p>People who are able to reframe difficult
      situations generally feel less burden and
      more in control. Feeling a greater degree   
      of control often leads to acting in control.
      Clara is a good example. Clara had difficulty taking breaks from
      caregiving. Before becoming a caregiver,
      she had worked in a demanding position
      and had realized the importance of taking
      weekends off and vacations to refresh herself
      and cope better with work demands.
      When she started to view caregiving as
      a job, it made a difference in how she
      viewed breaks in caregiving. They became
      not only more acceptable, but a necessity.</p>
    <p>Julie also found that reframing a difficult
      situation reduced her stress and helped her
      act in new ways. Julie felt resentful and burdened with
      the increasing demands of caring for her
      mother. She had no help, feeling that as a
      good, dutiful daughter she should do it all.
      A social worker told her about available
      resources and suggested she think of
      herself as a personal care manager as a way
      to find help in caregiving. Julie gained a
      sense of control over the situation once she
      realized she didn't have to provide all of
      the care herself, but could oversee her
      mother's care.</p>
    <p>As a caregiver, you may feel overwhelmed
      and burned out, especially if you are assuming
      responsibility for most of the caregiving.
      Changing your perception of your role
      from a caregiver to care manager is a way of
      reframing. As a care manager you still get
      the job done, but you don't have to provide
      all the care yourself. The role of care
      manager means that you:</p>
    <ul>
      <li>coordinate and supervise another's care
        needs. This includes using available
        support.</li>
      <li>are aware of available community
        resources.</li>
      <li> plan and prioritize care. </li>
      <li>understand the disease of the care receiver
        and what to expect. </li>
      <li> participate as an equal partner with other
        health care professionals. </li>
      <li> are knowledgeable about the health care
        system.</li>
    </ul>
    <p>As a care manager you assume an active
      role and reach beyond giving hands-on
      care, to planning and coordinating care and
      using available resources. You will feel an
      increased sense of mastery as a successful
      care manager.</p>
    <p>Self-talk -</p>
    <p>Most stress management courses
      include learning how to use self-talk to
      .promote health. Self-talk is what we say to
      ourselves. As Ralph Waldo Emerson said,
      "A man is what he thinks about all day long."
      What do you think about all day long?
      What do you say to yourself? I's especially
      important to notice your self-talk when you
      suffer setbacks and when you feel anxious,
      angry; discouraged, or distressed. Negative
      self-talk statements often begin with the
      following phrases:</p>
    <ul>
      <li> I just can't do ...</li>
      <li> If only I could (or didn't) do ... </li>
      <li> I could never. .. </li>
      <li> I shouldn't have done ... </li>
      <li> I should have ... </li>
    </ul>
    <p>Negative self-talk is defeating. It can lead
      to depression and a sense of failure, because
      with negative self-talk we tend to focus on: </p>
    <ul>
      <li> what we did not do versus what we have
        done. </li>
      <li>what we cant do versus what we can do. </li>
      <li>Our mistakes and failures versus our
        successes.</li>
    </ul>
    <p>You want your self-talk to work for you,
      not against you. If your self-talk is negative
      or unhelpful, challenge it. Learn to change
      the negative things you say to yourself into
      positive statements, such as affirmations.</p>
    <p>Affirmations are positive, supportive
      statements that counteract the effects of
      negative thinking. When positive statements
      are repeated several times a day, they begin
      to replace negative thoughts. This helps to
      change one's attitude, promote relaxation,
      and reduce stress. Karen's story is an example
      of changing negative self-talk to positive
      self-talk with the use of affirmations:</p>
    <p>Karen felt angry and discouraged when
      her mother didn't eat the tasty, nutritious
      meals she prepared for her. She didn't
      accept the fact that her mother's lack of
      appetite was caused by the illness. Karen
      constantly told herself, "No matter what I
      cook, it is never good enough for mother."</p>
    <p>This is an example of negative self-talk.
      Karen became aware that she often thought
      she was not doing good enough, especially
      in caring for her mother. These thoughts
      made her feel like a failure.
      With determination, patience, and practice,
      you can change your self-talk from
      negative to positive. The following steps
      lead to change:</p>
    <ul>
      <li>Identify your negative thoughts. Listen
        to what you say to yourself, especially
        during difficult times.</li>
      <li> Write your negative thoughts down on
        paper. This helps to identify and clarify
        them.</li>
      <li>Challenge your negative thoughts. Give
        them a good argument.</li>
      <li> Write a simple, positive statement for
        each thought you want to change. </li>
      <li> Memorize and repeat the chosen statements.
        This helps establish the habit of
        positive self-talk. </li>
      <li> Put your written-statements where you
        see them frequently. This is a helpful
        visual reminder.</li>
    </ul>
    <p>Karen chose the affirmation , "I am
      preparing nutritious food. That is enough."
      In fact, the statements, "I am doing my best.
      It is good enough," became her frequent
      affirmation and counteracted her negative
      thoughts of "not doing good enough."</p>
    <p>These statements have the dual. purpose of
      affirming what Karen is doing and helping
      her let go of the idea that she has control
      over her mother's appetite. Accepting that
      . was important. Telling herself that she is
      doing her best and it is enough is a positive
      way of saying she is changing what she can
      and letting go of what she cannot change.
      Karen's expectations for herself have
      become more realistic.</p>
    <p>Practice over time will change negative,
      habitual thinking. Repeat this activity
      frequently to identify other negative self-talk
      Remember, thoughts and attitudes
      create your reality. Changing your negative
      thoughts will help you focus on the positive
      things in your life, rather than on what you
      don't have.</p>
