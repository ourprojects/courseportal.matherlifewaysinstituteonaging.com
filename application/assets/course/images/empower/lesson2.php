  <p>This lesson explores the stress of caregiving. It will help you identify and understand your particular stressors, challenges, and strengths. You can then plan strategies that help you cope, change, and reduce stress. A basic premise of this chapter is that each of us has a reservoir of strength. The challenge is to identify our strengths build on them.</p>
  <h1>The Stress of Caregiving</h1>
  <div class="content">
    <p> There has been so much written about stress it has become a household word. Studies show that a certain amount of stress is helpful. It can challenge us to change and motivate us to do things we might not do otherwise. However, when the amount of stress overwhelms our ability to cope with
      it, we feel 'distress' or 'burnout.'</p>
    <p>According to <a href="http://www.merriam-webster.com/dictionary/distress" target="_blank">Webster's Dictionary</a>, distress
      is 'suffering of mind or body; severe physical or mental strain.' As a caregiver, you no doubt have increased stress in your
      life, whether you are caring for a mother with early Parkinson's disease, who is still able to care for her personal needs, or a spouse who doesn't recognize you because of advanced Alzheimer's disease.</p>
    <p class="blog">Write you own definition of 'distress' on the Forum.</p>
    <p>Each caregiving situation is unique. What is stressful for you may not be stressful for someone else. In his book The Survivor Personality, AI Siebert says, "there is no
      stress until you feel a strain." Since the feeling of stress is subjective and unique to each individual, it is difficult to define objectively. The stress you feel is not only the result of your caregiving situation, it is all of your perception of it. Your stress will
      increase or decrease depending on how you perceive your circumstances. And your perception will affect how you respond.</p>
    <p class="subheader">Factors That Affect Stress</p>
    <p> Your level of stress is influenced by many factors, including: </p>
    <ul>
      <li> whether your caregiving is voluntary or not: </li>
      <li> your relationship with the care receiver.</li>
      <li> your coping abilities.</li>
      <li> your caregiving situation. </li>
      <li> whether support is available.</li>
    </ul>
    <p>Whether your caregiving
      is voluntary or not  
      -
    <p>Many people become caregivers voluntarily. Others acquire the role because no one else is available. When you become a caregiver voluntarily, you are making a choice. However, if you "inherited" the job and feel you had no choice, the chances are greater for experiencing strain, distress,
      and resentment. Nancy became a caregiver because no one else was available.
    <p>Nancy couldn't have been more surprised when the visiting nurse asked her if she was the primary caregiver for her mother in-law, Joan. Nancy was fond of Joan. She called and stopped by frequently to see how Joan was managing, but hadn't thought of herself as the primary caregiver. It was apparent that Joan's medical condition was worsening and she was becoming increasingly weak Nancy realized there were no other children or relatives available, so she agreed, although somewhat reluctantly, to be Joan's caregiver. Nancy felt anxious and uncertain about what it meant to be a primary caregiver and whether she had the necessary skills to perform the role.
    <p>Luckily, Nancy and Joan had a good relationship and they were able to communicate openly, minimizing some of the potential for stress. You can't always think about a caregiving relationship in advance, but if you can, it has greater potential for success.</p>
    <p>Your relationship with the care receiver -</p>
    <p>If your relationship with the care receiver has been difficult, becoming a caregiver is more of a challenge. If the care receiver has always been demanding and controlling, you will probably feel more stress, anger, and resentment. Sometimes people are caregiving with the hope of healing a relationship. The healing may or may not happen. If healing doesn't happen, the caregiver may feel regret, depressed, and discouraged. A professional counselor, spiritual advisor, or trusted friend can help deal with such feelings and emotions.</p>
    <p>Your coping abilities -</p>
    <p>How you have coped with stress in the past predicts how you will cope now. Did you find constructive ways to manage your stress? Perhaps you were able to find time to exercise regularly and generally take care of yourself. Or did you rely on alcohol or drugs to help you cope? Sometimes people rely on medications and alcohol in times of stress, which only makes matters worse. It is important to identify your current coping strengths and build on them. Learning new coping skills also will help make your caregiving situation less stressful.</p>
    <p>The caregiving situation - </p>
    <p>What does your caregiving situation require of you? Does it require 24-hour-aday availability? Or do you just need to make an occasional telephone call to check on the person? What disease does the care receiver have? Does he have a mental or physical disability, or both? Certain caregiving situations are more stressful than others. For example, caring for someone who has a dementia such as Alzheimer's disease is often more stressful than caring for someone with a physical limitation. Also, stress tends to be highest when:</p>
    <ul>
      <li>the caregiving situation continues for a long time.</li>
      <li>the care receiver's needs gradually increase.</li>
      <li> caregivers feel they have limited or no support.</li>
      <li>caregivers have their own health/physical problems.</li>
    </ul>
    <p>Whether support is available -</p>
    <p>Caregivers who feel isolated and without adequate support usually experience a higher level of stress. Support may be lacking for several reasons:</p>
    <ul>
      <li>The caregiver may resist accepting help, even when he or she needs it.</li>
      <li> Others may be willing to help but don't offer because they are uncomfortable around the ill person, frightened of the illness, or don't know what they can do.</li>
      <li>Others don't want to interfere, especially if the caregiver seems to have everything under control and has refused help in the past.</li>
    </ul>
    <p>Caregiver stress is influenced by many factors, including the need to adapt to ongoing changes and losses caused by the care receiver's illness. These changes cause you to redefine your life. What was normal has changed. You are living with a new reality.</p>
