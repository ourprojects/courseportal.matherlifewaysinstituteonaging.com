      our breathing becomes. · Stress management tools usually include
      a focus on breathing. The following
      breathing exercise takes only one or two
      minutes and you can easily do it anywhere.
      Use it often to lower stress.</p>
    <p>Meditation -</p>
    <p>The word "meditation"
      comes from the Sanskrit word medha which,
      when taken literally, means "doing the
      wisdom." Meditation aids in relaxation and
      in achieving physical and mental well-being.
      Meditation is
      keeping your
      attention focused in
      the moment to
      quiet the mind and
      hear your body's
      inner wisdom. You,
      too, can learn to
      meditate. See the
      "Process of
      Meditation" box on the next page.</p>
    <p>Music -</p>
    <p>Music is another tool for reducing
      stress. It can alter the body and the mind.
      It can induce deep relaxation, act as a
      stimulant, and take you into other states
      of consciousness. Music is often used
      specifically for healing and decreasing stress
      and tension. Use the following steps as a
      guideline. </p>
    <ol>
      <li> Choose soothing music you like.</li>
      <li> Relax and close your eyes. </li>
      <li> Breathe deeply and easily. </li>
      <li> Lose yourself in the music, listening with
        your body, not your mind. </li>
      <li> After the music is finished, open your
        eyes and notice how you feel. </li>
    </ol>
    <p>Music is a universal language. Listening
      to music can be healing for both you and
      the care receiver, either together or alone.
      People with dementia, especially, respond to
      music when they may respond to little else.</p>
    <p>Humor -</p>
    <p> Caregivers who maintain and
      foster their sense of humor do better. It is
      , often hard to find much that is humorous
      in caregiving, but the secret to succeeding
      as a caregiver is to find humor in your daily
      routine. Finding humor does not deny the
      fact that, at times, your heart is heavy with
      the pain and sadness of caregiving. Those
      times will exist, but they can co-exist with
      laughter and humor.</p>
    <p>Tears and laughter are closely related.
      They each offer a release of tension and are
      often intermingled. Humor does not
      minimize the seriousness of a situation;
      rather, it helps you embrace it.
      Humor can be a helpful tool in many
      ways, from making us laugh at our
      shortcomings and impossible situations,
      reducing anxiety and stress. Laughter
      relaxes and helps calm emotions, allowing
      us to regain emotional balance and think
      more clearly. If you want to laugh, or want
      someone else to laugh, you may have to
      find a reason, as George and Alma do.</p>
    <p> George and Alma watch their favorite
      comedy show on television every weeknight
      at 7 P.M. They look forward to it and
      anticipate laughing together. In addition,
      Alma and George look for humorous cartoons
      and jokes to share with each other.
      The fact that Alma has a disabling medical
      condition doesn't mean they can't appreciate
      laughter.</p>
    <p>In his book Anatomy of an Illness, Norman
      Cousins wrote of his fight against a crippling
      disease. He credited his recovery to the
      use of laughter. He intentionally sought
      healing through watching videotapes of
      comedies, reading joke books, and listening
      to people tell jokes. He had read about the
      effects of stress and emotions on illness.
      He understood that disease was caused by
      chemical changes in the body, due to the
      stress of strong emotions such as fear and
      anger. He concluded that perhaps love,
      laughter, hope, and the will to live would
      counteract those effects. He was right in his belief. Recent studies show that laughter
      helps to stimulate breathing, muscular
      activity; and heart rate. This serves to reduce
      stress and strengthen the immune system.</p>
    <p>Humor is important to health. It lifts the
      spirit and provides a way to connect with
      others. The following suggestions can help
      · you make laughter and humor a larger part
      of your life:</p>
    <ul>
      <li> Seek out humor. Humorous tapes and
        books can be found at video stores and
        libraries. Spend time with friends or family
        members you enjoy and can laugh with. </li>
      <li> Surround yourself with humor. Put jokes,
        cartoons, funny pictures, and humorous
        sayings on the refrigerator or bulletin board
        where others can enjoy them with you. </li>
      <li> Laugh at yourself. Don't take yourself
        too seriously Poke fun at yourself by
        making light of your shortcomings
        (which we all have).</li>
    </ul>
    <p>Developing action plans -</p>
    <p>Action plans are tools for change. They can be a useful way to identify and plan specific activities for reducing stress and making change: Feelings of accomplishment are necessary for thriving as a caregiver. Action plans can help you achieve these feelings. Even the smallest action can make a big difference. This was true for Evelyn.</p>
    <p>Evelyn needed more time for herself during the day She made a plan to take a leisurely; warm tub bath four times a week instead of the always-hurried shower. Evelyn settled her father to watch the news on TV when she took her baths. This worked well for both of them and became an accepted part of their routine. Accomplishing the action plan encouraged Evelyn to make other action plans that made a big difference to her. </p>
    <p> Feelings of mastery and confidence are usually the result of developing new ways of coping. Use the information presented in this chapter to help you identify your stressors, and improve coping skills. The activity in the box on the next page can be a useful tool for managing stress.
      This activity can be useful on a regular basis. It will help you assess and cope with current stressors. Since your caregiving situation and stressors continually change, it is important to be aware of when you feel stress and to use stress-reducing tools that work for you. Most important, build stress reduction and nurturing activities into daily life to prevent distress. Be proactive and remember, what is good for you is good for the person receiving care!</p>
    <p>Finding hope and meaning -</p>
    <p>The ability to find hope and meaning in
      the caregiving situation enables you not
      only to survive, but to thrive. Finding meaning
      and hope are what keeps us going. It is a
      way to make sense of our circumstances.</p>
    <p> In his book Mans Search For Meaning,
      psychiatrist Viktor Frankl tells of his
      experience as a long-time prisoner in a
      prisoner of war camp during World War II.
      Many of his family members died in the
      camps. In spite of the fact that he faced death constantly and suffered severe
      punishment, Dr. Frankl was able to find
      meaning and hope in his life. He noted that
      the prisoners who were able to sustain even
      a flicker of hope were better able to survive
      the terrible circumstances than those who
      felt hopeless. He concluded that what did
      remain, when all else was taken away; was
      "the last of the human freedoms," the ability
      to "choose one's attitude in a given set of
      circumstances." Out of that experience,
      Frankl's guiding philosophy was born: "To
      live is to suffer, to survive is to find
      meaning in the suffering." He also believed
      that man's need for meaning is universal.</p>
    <p>The need to find hope and meaning is
      also important when you are a caregiver
      for a person with
      a chronic illness.
      Uncertainty; loss, and
      suffering may shake
      your foundation.
      After all, you have
      much at stake. Your
      world, as you have known it, has changed
      drastically and you may be left with
      questions such as, "Why me?" and perhaps,
      "Where is God?" Questioning often leads to
      a search for meaning. No one else can tell
      you what the meaning is for you. It can be a
      lonely journey.</p>
    <p class="blog">REFRAMING YOUR STRESS</p>
    <p class="blog">Make a list of those things that you find most
      difficult or stressful. Be specific. Write at least
      two (more if you can).</p>
    <p class="blog"> Answer the following questions in relation to
      each item on your list.
      Can I ignore this? Or can I let it go?</p>
    <p class="blog">Can I change anything about this? If so, how can I change it?</p>
    <p class="blog"> If it can't be changed, can I change my perception of it? If so, how? What is a more helpful perception?</p>
    <p class="blog"> Select one stressor from your list to work on
      first. The stressor is: Develop an action plan for addressing this stressor. Be specific and realistic.</p>
    <p>A search for meaning can be a conscious choice. There are ways to stimulate your search. The following can be helpful:</p>
    <ol>
      <li>Ask yourself questions like "What am I to learn from this?" What good can come from this? Am I a better person now? These types of questions can help you open up to possibilities for finding meaning.</li>
      <li> Reflect .. Periods of quiet reflection, especially after a difficult time, are important and offer opportunities to learn from the experience.</li>
      <li>Talk with a trusted person. Whether this person is a counselor, religious advisor, or friend, sharing can help clarify your thoughts and feelings. As you tell
        your story; it often takes on meaning.</li>
      <li>Write. This is also a way to clarify your thinking. Writing is a way to bring out your thoughts and feelings. Write freely and spontaneously. Don't concern yourself with proper sentence structure or punctuation. Writing is a way to talk to yourself. Re-reading your journal over time provides an understanding of where you were when you started and where you are now. You will probably see changes and find new 
        understanding and meaning.</li>
      <li>Seek spiritual renewal. This is especially important when you are facing difficult times. Many caregivers report that faith and prayer help them find comfort, purpose, and meaning. It may be that even when you feel anger because of suffering and sorrow, your need for meaning is greatest.</li>
    </ol>
    <p>Like Frankl, it is hopeful to believe that meaning can be found in difficult and painful experiences. Hope and meaning play a large part in the following story of Margaret and Tim.</p>
    <p>Tim's frequent visits to his elderly mother, Margaret, in the nursing home, were meaningful to him. Years ago, when Margaret was healthy; she shared some of her beliefs with Tim. She had told him, "If there comes a time when I am not able to recognize you because of Alzheimer's disease, or for any other reason, I want you to know what I believe to be true. I believe that my true essence, my spirit, will always be present, even though my physical body and mind may not be the person you  remember. Please know that I am with you. We may not be able to talk with each other as we did in the past, but if you play my favorite music, read poetry, hold my hand, or just be with me, I will feel your love and you will feel mine for you."</p>
    <p>In sharing her beliefs, Margaret gave Tim the gift of finding meaning in what can be a most difficult and challenging situation. Meaning is all around us. It is the "stuff'' of life. Meaning is personal. It is up to each
      persc1n to find his or her own meaning.</p>
