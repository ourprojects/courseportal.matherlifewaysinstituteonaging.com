    <p>Getting social support -</p>
    <p>Caregiving can be a lonely experience.
      According to the National Family
      Caregivers Association, caregivers often ·
      report that they feel alone and isolated.
      Support from family, friends, and others
      is an important stress buffer. Something as
      simple as a two-minute telephone call can
      make you feel cared about and supported.
      It helps to share your experiences and
      burdens with a person you trust-a friend,
      family member, counselor, religious advisor,
      or support group member-who will listen
      and understand.</p>
    <p>Support groups can be helpful when
      you're going through a difficult time.
      Sharing with others who are going through
      similar experiences is a way to give and
      receive support, and take time out from
      caregiving duties. You
      can learn new ways of
      coping from others in the
      group, which may include
      learning to look at the
      light side of difficult
      situations with a bit of
      humor. Sharing lightens
      the load. A support group
      is a place to express
      thoughts and feelings in a
      confidential setting. Most
      important, you learn that
      you are not alone. This
      can be a wonderful relief.
      Support groups are
      available for caregivers
      and for people with various chronic
      illnesses. Local hospitals and disease-related
      associations often have groups available.</p>
